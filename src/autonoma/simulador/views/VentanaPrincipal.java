    private void btnFrenarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFrenarMouseClicked
    // Verifica si el motor está apagado
    if (!vehiculo.motor.isEncendido()) {
        JOptionPane.showMessageDialog(null, "No puedes frenar un vehículo apagado");
    } else {
        try {
            // Obtener el valor de frenar del campo de texto (puede ser txtFrenar en lugar de txtAcelerar)
            int frenar = Integer.parseInt(txtFrenar.getText());

            // Verificar que la frenada no haga que la velocidad sea negativa
            if (frenar > velocidad) {
                JOptionPane.showMessageDialog(null, "El vehículo ha patinado, al frenar mas que la velocidad");
                    JOptionPane.showMessageDialog(null, "Se detuvo por completo el vehiculo, recuperando el control");
                velocidad = 0; // La velocidad no puede ser menor que 0
            } else {
                velocidad -= frenar; // Reducir la velocidad por el valor de frenado
            }

            // Llamar al método frenar del objeto vehiculo
            vehiculo.frenar(velocidad);

            // Actualizar el campo de texto que muestra la velocidad
            txtVelocidad.setText(String.valueOf(velocidad));

        } catch (NumberFormatException ex) {
            // Si el texto ingresado en txtFrenar no es un número válido
            JOptionPane.showMessageDialog(null, "Por favor, ingresa un número válido en el campo de frenado");
        } catch (VehiculoApagadoBloqueadoException ex) {
            // Manejo de la excepción si el vehiculo no debería poder frenar
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (VehiculoDetenidoException ex) {
            // Manejo de excepción si el vehiculo ya está detenido
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "El vehículo ya está detenido.");
        } catch (VehiculoPatinaraException ex) {
            // Manejo de excepción si el vehiculo patina
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Se patino");
        }
    }
    }//GEN-LAST:event_btnFrenarMouseClicked

