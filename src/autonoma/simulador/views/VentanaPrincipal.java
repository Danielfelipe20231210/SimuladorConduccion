    private void btnAcelerarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAcelerarMouseClicked
       // Verifica si el motor está apagado
    if (!vehiculo.motor.isEncendido()) {
        JOptionPane.showMessageDialog(null, "No puedes acelerar un vehículo apagado");
    } else {
        try {
            // Verificar si el campo de aceleración está vacío
            if (txtAcelerar.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor, ingresa un valor en el campo de aceleración.");
                return; // Salir del método si el campo está vacío
            }

            // Obtener el valor de aceleración del campo de texto
            int aceleracion = Integer.parseInt(txtAcelerar.getText());

            // Validar que la aceleración sea positiva
            if (aceleracion <= 0) {
                JOptionPane.showMessageDialog(null, "La aceleración debe ser un valor positivo");
                return;
            }

            // Actualizar la velocidad del vehículo sumando la aceleración
            velocidad += aceleracion;

            // Verificar si la velocidad supera un límite máximo (opcional)
            if (velocidad > 200) {  // Límite de velocidad de 200 km/h, ajustable según el simulador
                velocidad = 0;
                JOptionPane.showMessageDialog(null, "Has superado el limite del motor, El vehiculo se accidento");
            }

            // Llamar al método acelerar del objeto vehiculo
            vehiculo.acelerar(velocidad);

            // Actualizar el campo de texto que muestra la velocidad
            txtVelocidad.setText(String.valueOf(velocidad));

        } catch (NumberFormatException ex) {
            // Si el texto ingresado en txtAcelerar no es un número válido
            JOptionPane.showMessageDialog(null, "Por favor, ingresa un número válido en el campo de aceleración.");
        } catch (VehiculoApagadoBloqueadoException ex) {
            // Manejo de la excepción si el vehículo no debería poder acelerar
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnAcelerarMouseClicked

